# RISK ANALYZER FUNCTION
def analyze_job_risk(text, company_profile, salary, has_logo):
    reasons = []
    risk_score = 0

    text = str(text).lower()

    urgent_words = [
        "urgent", "immediately", "asap",
        "limited", "hiring now", "quickly"
    ]

    money_words = [
        "earn", "income", "bonus",
        "salary", "weekly", "daily"
    ]

    fee_words = [
        "fee", "registration",
        "deposit", "payment", "processing fee"
    ]

    if any(word in text for word in urgent_words):
        reasons.append("⚠️ Urgency keywords detected")
        risk_score += 20

    if any(word in text for word in money_words):
        reasons.append("💰 Unrealistic earning related words found")
        risk_score += 15

    if any(word in text for word in fee_words):
        reasons.append("💳 Registration / Processing fee detected")
        risk_score += 25

    if len(str(company_profile).strip()) < 30:
        reasons.append("🏢 Company profile is too short or missing")
        risk_score += 20

    if has_logo == 0:
        reasons.append("🖼 Company logo not available")
        risk_score += 10

    if salary == "":
        reasons.append("💵 Salary information missing")
        risk_score += 10

    risk_score = min(risk_score, 100)

    return reasons, risk_score

    
