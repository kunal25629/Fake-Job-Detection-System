# For Generating PDF
def generate_pdf_report(job_title, prediction, confidence,
                        risk_score, reasons, shap_explanations):

    buffer = BytesIO()

    doc = SimpleDocTemplate(buffer)

    styles = getSampleStyleSheet()

    story = []

    story.append(Paragraph("<b>FAKE JOB ANALYSIS REPORT</b>", styles['Title']))
    story.append(Paragraph("<br/><br/>", styles['Normal']))

    story.append(Paragraph(f"<b>Job Title:</b> {job_title}", styles['Normal']))
    story.append(Paragraph(f"<b>Prediction:</b> {prediction}", styles['Normal']))
    story.append(Paragraph(f"<b>Confidence:</b> {confidence:.2%}", styles['Normal']))
    story.append(Paragraph(f"<b>Risk Score:</b> {risk_score}/100", styles['Normal']))

    story.append(Paragraph("<br/><b>Risk Factors</b>", styles['Heading2']))

    if reasons:
        for reason in reasons:
            story.append(Paragraph(f"• {reason}", styles['Normal']))
    else:
        story.append(Paragraph("No suspicious indicators found.", styles['Normal']))
    
    
    # AI Model Explanation (SHAP)
    story.append(Paragraph("<br/><b>AI Model Explanation (SHAP)</b>", styles['Heading2']))
    
    if shap_explanations:
        for explanation in shap_explanations:
            story.append(Paragraph(f"• {explanation}", styles['Normal']))
    else:
        story.append(Paragraph("No SHAP explanation available.", styles['Normal']))
    
    
    # Recommendation
    story.append(Paragraph("<br/><b>Recommendation</b>", styles['Heading2']))


    if risk_score >= 70:
        recommendation = "High Risk. Verify the company before applying."
    elif risk_score >= 40:
        recommendation = "Medium Risk. Review the job carefully."
    else:
        recommendation = "Low Risk. Job appears relatively safe."

    story.append(Paragraph(recommendation, styles['Normal']))

    story.append(Paragraph("<br/><br/>Generated by AI Fake Job Detection System",
                           styles['Italic']))

    doc.build(story)

    pdf = buffer.getvalue()

    buffer.close()

    return pdf


# Resume opening function for PDF
def extract_pdf_text(file):

    text = ""

    with pdfplumber.open(file) as pdf:

        for page in pdf.pages:

            page_text = page.extract_text()

            if page_text:

                text += page_text

    return text

# Resume opening function for DOCX
def extract_docx_text(file):

    doc = Document(file)

    text = "\n".join(
        para.text for para in doc.paragraphs)

    return text

